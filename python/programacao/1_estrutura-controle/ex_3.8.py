# Exercício 3.8 - Escreva um programa que leia um valor em metros e o exiba conver-
# tido em milímetros.

valor_em_metros = int(input('Informe a quantidade de metros(m) para transformar em milímetros(mm): '))

valor_em_milimetros = valor_em_metros * 1000

print(f'{valor_em_metros}m convertido para milímetros é {valor_em_milimetros}mm')
