numero = int(input())

if numero not in [61, 71, 11, 21, 32, 19, 27, 31]:
  print('DDD nao cadastrado')
else:
  if numero == 61:
    print('Brasilia')
  elif numero == 71:
    print('Salvador')
  elif numero == 11:
    print('Sao Paulo')
  elif numero == 21:
    print('Rio de Janeiro')
  elif numero == 32:
    print('Juiz de Fora')
  elif numero == 19:
    print('Campinas')
  elif numero == 27:
    print('Vitoria')
  elif numero == 31:
    print('Belo Horizonte')
